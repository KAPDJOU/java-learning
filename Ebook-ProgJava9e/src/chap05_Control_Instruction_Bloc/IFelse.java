package chap05_Control_Instruction_Bloc;

public class IFelse {

    public static void main(String[] args) {

        int i = 58;
        if (i < 100 && i > 100) {
            System.out.println("Le nombre est bien dans l'intervalle.");
        } else {
            System.out.println("Le nombre n'est pas dans l'intervalle.");
        }
    }
}
